<h2 class="h2">About</h2>

<section class="w-16 md:w-1/2 grid gap-4 justify-items-center text-center">
	<p>
		This application is to help with sprint planning for your agile needs. You can create a "room"
		which are public by default. This means others can see any other rooms that have been created.
		There is no permanent data for this applications after 24 hours it will be cleaned up.
	</p>

